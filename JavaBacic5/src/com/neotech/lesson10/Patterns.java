package com.neotech.lesson10;

public class Patterns {

	public static void main(String[] args) {

		for(int row = 0; row < 5; row++)
		{
			for(int col = 0; col <= row; col++)
			{
				System.out.print("*");
			}
			System.out.println("");
		}
	
	
	
	
	for (int x = 4; x >= 1; x--)
	{
		for(int y = 0; y < x; y++)
		{
			System.out.print("*");
		}
		System.out.println();
	} 

	
	
	
	
	}
}
