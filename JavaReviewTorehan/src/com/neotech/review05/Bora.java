package com.neotech.review05;

public class Bora {
	
	//A method with NO parameters and NO return types
	void addTwoNumbers() {
		int result = 3+2;
	}
	//A method with 2 parameters and NO return type
	void addTwoNumbersThatIGive(int a, int b) {
		int result = a+b;
	}
	//A method with NO parameters and YES return type
	int addTwoNumbersAndGiveMeTheResult() {
		int result = 3+2;
		return result;
	}
	//A method with 2 parameters and YES return type
	int addTwoNumbersThatIGiveAndGiveMeTheResult(int num1, int num2) {
		int result = num1 + num2;
		return result;
	}
}
